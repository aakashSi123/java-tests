import java.util.Scanner;
public class forloop {
    public static void main(String[] args) {
        for(int i=1;i<=2;i++)
        {
            for(int j=1;j<=2;j++)
        {
            Scanner sc=new Scanner(System.in);
            int a = sc.nextInt();
            System.out.println(a);
        }
        }
    }

}
